public class Super {
    private String nombreCampusero,Apellidos,cedula;

    public Super(String nombreCampusero, String Apellidos, String cedula) {
        this.nombreCampusero = nombreCampusero;
        this.Apellidos = Apellidos;
        this.cedula = cedula;
    }

    public String getNombreCampusero() {
        return nombreCampusero;
    }

    public String getApellidos() {
        return Apellidos;
    }

    public String getCedula() {
        return cedula;
    }
    

   
    
}
